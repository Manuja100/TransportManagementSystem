package com.Manager;

public class vehicle {
	
	private int id;
	private String regno;
	private String seat;
	private String type;
	
	
	public vehicle(int id, String regno, String seat, String type) {
		
		this.id = id;
		this.regno = regno;
		this.seat = seat;
		this.type = type;
	}


	public int getId() {
		return id;
	}


	
	public String getRegno() {
		return regno;
	}



	public String getSeat() {
		return seat;
	}



	public String getType() {
		return type;
	}


	
	
	
	
	

}
