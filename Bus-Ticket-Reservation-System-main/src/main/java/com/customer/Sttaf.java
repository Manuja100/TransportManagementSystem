package com.customer;

public class Sttaf {
	
	private int empID;
	private String nmae;
	private String email;
	private  String phone ;
	private String password;
	private String positin;
	
	
	public Sttaf(int empID, String nmae, String email, String phone, String password, String positin) {
		
		this.empID = empID;
		this.nmae = nmae;
		this.email = email;
		this.phone = phone;
		this.password = password;
		this.positin = positin;
	}


	public int getEmpID() {
		return empID;
	}


	


	public String getNmae() {
		return nmae;
	}


	


	public String getEmail() {
		return email;
	}


	

	public String getPhone() {
		return phone;
	}


	

	public String getPassword() {
		return password;
	}





	public String getPositin() {
		return positin;
	}


	
	
	
	
	
	
	
	

}
